package hypeercube;

public class Insert {

	public String hashObj;
	public String keyword;
	
	public Insert(String hashObj, String keyword) {
		this.hashObj=hashObj;
		this.keyword=keyword;
	}
	
	
	public String getHashObj() {
		return this.hashObj;
	}
	
	public String getKeyword() {
		return this.keyword;
	}
	
	
}
